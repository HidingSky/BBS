package com.sun.entity;

public class Tiezi {

    private String id;
    private String biaoti;
    private String neirong;
    private String fatieren;
    private String bangdingbankuai;

    public String getBiaoti() {
        return biaoti;
    }

    public void setBiaoti(String biaoti) {
        this.biaoti = biaoti;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNeirong() {
        return neirong;
    }

    public void setNeirong(String neirong) {
        this.neirong = neirong;
    }

    public String getFatieren() {
        return fatieren;
    }

    public void setFatieren(String fatieren) {
        this.fatieren = fatieren;
    }

    public String getBangdingbankuai() {
        return bangdingbankuai;
    }

    public void setBangdingbankuai(String bangdingbankuai) {
        this.bangdingbankuai = bangdingbankuai;
    }
}
