package com.sun.entity;

public class Huitie {

    private String id;
    private String huitieneirong;
    private String yonghu;
    private String bangdingtiezi;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }


    public String getYonghu() {
        return yonghu;
    }

    public void setYonghu(String yonghu) {
        this.yonghu = yonghu;
    }

    public String getBangdingtiezi() {
        return bangdingtiezi;
    }

    public void setBangdingtiezi(String bangdingtiezi) {
        this.bangdingtiezi = bangdingtiezi;
    }

    public String getHuitieneirong() {
        return huitieneirong;
    }

    public void setHuitieneirong(String huitieneirong) {
        this.huitieneirong = huitieneirong;
    }
}
